// Problem 8: ধর, তুই তোদের বাসায় মোট 20 টা ডিম আছে । কিন্তু প্রতিদিন তুই 2 টা ডিম ভাঙ্গা হয় । একটা ফাংশন লিখ, যেখানে প্রতিদিনের ডিমের সংখ্যা কমে কনসোলে দেখানো হবে যতক্ষণ ডিম শেষ না হয়।

function eggCount() {
  let eggs = 20;
  while (eggs > 0) {
    console.log("বাকি ডিম:", eggs);
    eggs -= 2;
  }
}
eggCount(); // Output: প্রতিদিন ২টা করে ডিম কমে দেখাবে যতক্ষণ না শেষ হয়
