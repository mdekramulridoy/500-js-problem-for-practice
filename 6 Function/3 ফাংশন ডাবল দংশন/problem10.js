// Problem 10: Compare lengths of two names
function isFirstNameLonger(name1, name2) {
  // Return true if first name is longer, false otherwise
  return name1.length > name2.length;
}
console.log(isFirstNameLonger("Tanvir", "Jim")); // Output: true
console.log(isFirstNameLonger("Jahid", "Hadi")); // Output: false
